// Basic Operations
#include <iostream>
using namespace std;

int main(){
    cout << "Enter 2 numbers = " << endl;
    int n1 = 0, n2 = 0;
    cin >> n1 >> n2;
    cout << "Sum of " << n1 << " and " << n2 << " are " << n1 + n2 << endl;
    cout << "Difference of " << n1 << " and " << n2 << " are " << n1 - n2 << endl;
    cout << "Product of " << n1 << " and " << n2 << " are " << n1*n2 << endl;
    return 0;
}

