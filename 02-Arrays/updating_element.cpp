//Updating Element in ARRAY
#include <iostream>
using namespace std;

int main(){
    int arr[5] = {99, 90, 92, 94, 95};
    arr[0] = 101;
    cout << "Updated Marks >> " << endl;
    cout << arr[0] << endl;
    cout << arr[1] << endl;
    cout << arr[2] << endl;
    cout << arr[3] << endl;
    cout << arr[4] << endl;
    return 0;
}
